export default function generateSlug(): string {
  const pick = () =>
    SLUG_ARRAY[Math.floor(Math.random() * SLUG_ARRAY.length)] || "";

  const wordCount = 3;
  const selected = new Set<string>();

  while (selected.size < wordCount) {
    selected.add(pick());
  }

  const randomSuffix = Math.floor(1000 + Math.random() * 9000);

  return `${[...selected].join("-")}-${randomSuffix}`;
}
